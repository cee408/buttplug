CREATE TABLE device (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  identifier TEXT,
  device_name TEXT NOT NULL,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
