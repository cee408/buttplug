CREATE TABLE protocol_usb (
  protocol_id INTEGER,
  usb_vendor_id INTEGER,
  usb_product_id INTEGER,
  PRIMARY KEY(protocol_id, usb_vendor_id, usb_product_id),
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
