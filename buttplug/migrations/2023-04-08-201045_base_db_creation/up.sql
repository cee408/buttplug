PRAGMA foreign_keys = ON;

CREATE TABLE protocol (
  id INTEGER PRIMARY KEY,
  protocol_name TEXT NOT NULL UNIQUE,
  display_name TEXT NOT NULL
);

CREATE TABLE protocol_bluetooth_name (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  bluetooth_name TEXT,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
);

CREATE UNIQUE INDEX protocol_bluetooth_name_unique ON protocol_bluetooth_name(protocol_id, bluetooth_name);

CREATE TABLE protocol_bluetooth_manufacturer_data (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  manufacturer_company INTEGER,
  manufacturer_data BLOB,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
);

CREATE TABLE protocol_bluetooth_prefix (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  prefix VARCHAR(255),
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION 
);

CREATE UNIQUE INDEX protocol_bluetooth_prefix_unique ON protocol_bluetooth_prefix(protocol_id, prefix);

CREATE TABLE protocol_bluetooth_service (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  service_uuid TEXT,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION   
);

CREATE TABLE protocol_bluetooth_characteristic (
  id INTEGER PRIMARY KEY,
  service_id INTEGER,
  endpoint TEXT,
  characteristic_uuid TEXT,
  FOREIGN KEY(service_id)
    REFERENCES protocol_bluetooth_service (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
);

CREATE TABLE protocol_xinput (
  protocol_id INTEGER PRIMARY KEY,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
);

