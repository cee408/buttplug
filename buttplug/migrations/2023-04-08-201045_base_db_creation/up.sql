CREATE TABLE user_protocol_serial (
  protocol_id INTEGER,
  port TEXT UNIQUE,
  baud INTEGER,
  data_bits INTEGER,
  stop_bits INTEGER,
  parity TEXT,
  PRIMARY KEY(protocol_id, port),
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
);

CREATE TABLE user_protocol_websocket_name (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  device_name TEXT,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
      ON DELETE CASCADE
      ON UPDATE NO ACTION
);

CREATE TABLE user_protocol_websocket_prefix (
  id INTEGER PRIMARY KEY,
  protocol_id INTEGER,
  device_name TEXT,
  FOREIGN KEY(protocol_id)
    REFERENCES protocol (id)
